export type ApiMessageKey = keyof typeof messages.en;

export function getRequestLocale(req: Request): ApiLocale {
  const explicit = String(req.headers["x-locale"] ?? req.headers["x-language"] ?? "").toLowerCase();
  if (explicit.startsWith("uz")) return "uz";
  if (explicit.startsWith("ru")) return "ru";

  const acceptLanguage = String(req.headers["accept-language"] ?? "").toLowerCase();
  if (acceptLanguage.startsWith("uz")) return "uz";
  if (acceptLanguage.startsWith("ru")) return "ru";
  return "en";
}

export function apiT(req: Request, key: ApiMessageKey, vars: Record<string, string | number> = {}) {
  const locale = getRequestLocale(req);
  let template: string = messages[locale][key] ?? messages.en[key] ?? key;

  for (const [name, value] of Object.entries(vars)) {
    template = template.replaceAll(`{${name}}`, String(value));
  }

  return template;
}
